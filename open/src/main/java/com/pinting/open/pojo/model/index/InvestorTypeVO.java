package com.pinting.open.pojo.model.index;

public class InvestorTypeVO {
	

	private String investorTypeName;
	private Integer investorTypeNumber;
	private String investorTypeNumberString;
	private Double investorTypeRate;
	private String investorTypeRateString;
	
	public String getInvestorTypeName() {
		return investorTypeName;
	}
	public void setInvestorTypeName(String investorTypeName) {
		this.investorTypeName = investorTypeName;
	}
	public Integer getInvestorTypeNumber() {
		return investorTypeNumber;
	}
	public void setInvestorTypeNumber(Integer investorTypeNumber) {
		this.investorTypeNumber = investorTypeNumber;
	}
	public String getInvestorTypeNumberString() {
		return investorTypeNumberString;
	}
	public void setInvestorTypeNumberString(String investorTypeNumberString) {
		this.investorTypeNumberString = investorTypeNumberString;
	}
	public Double getInvestorTypeRate() {
		return investorTypeRate;
	}
	public void setInvestorTypeRate(Double investorTypeRate) {
		this.investorTypeRate = investorTypeRate;
	}
	public String getInvestorTypeRateString() {
		return investorTypeRateString;
	}
	public void setInvestorTypeRateString(String investorTypeRateString) {
		this.investorTypeRateString = investorTypeRateString;
	}
	
}
